# --------------------------------------------------- DECLARACIÓN DE VARIABLES
num_one = 10
num_two = 5

# --------------------------------------------------- OPERADORES BÁSICOS EN PYTHON
print("Suma: {}".format(num_one + num_two))
print("Resta: {}".format(num_one - num_two))
print("Multiplicación: {}".format(num_one * num_two))
print("División: {}".format(num_one / num_two))
print("División (Retorna entero): {}".format(num_one // num_two))
print("Multiplicación (exponencial): {}".format(num_one ** num_two))

print("'==': {}".format(num_one == num_two))
print("'!=': {}".format(num_one != num_two))
